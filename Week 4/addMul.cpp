// This program find the sum and product of two integers.

#include <stdio.h>
#include <iostream>
using namespace std;

int main ()
{
    int num1, num2, sum, prod;
    num1 = 3;
    num2 = -1;
    sum = num1 + num2;
    prod = num1 * num2;
    cout << "The sum of " << num1 << " and " << num2 << " is " << sum << endl;
    cout << "The product of " << num1 << " and " << num2 << " is " << prod << endl;
    return 0;
    
    
}
